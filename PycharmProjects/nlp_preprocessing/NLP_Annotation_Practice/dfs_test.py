# 把队列结构改成栈结构就行，弹出队列最后一个
def DSF(graph, s):
    stack = [];
    seen = set();
    stack.append(s)
    seen.add(s)

    while (len(stack) > 0):
        vertex = stack.pop()  # 弹出最后一个元素
        nodes = graph[vertex]
        for w in nodes:
            if w not in seen:
                stack.append(w)
                seen.add(w)
        print(vertex)
    print(seen)


